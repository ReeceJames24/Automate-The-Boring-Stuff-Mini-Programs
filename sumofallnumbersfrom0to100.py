total = 0
for i in range(101):  #this for loops goes for 100 iterations then prints the total after
    total = total + i
print(total)

# notes
# for loops will loop a specific number of times
# the range() function is called with one, two, or three arguments (third one is called the step argument which is for the incremental increase or decrease you want per iteration)
# break and continue can be used in for loops

